#include "Resource.hpp"

////////////////////////////////////////////////////////////////////////////////

Resource::Resource():
    m_count()
{
}

Resource::~Resource()
{
}

void Resource::acquire()
{
    m_count += 1;
}

void Resource::release()
{
    if (m_count > 0)
    {
        m_count -= 1;
    }
    if (m_count == 0)
    {
        delete this;
    }
}

